  const [specs, setSpecs] = useState([]);
  const [view, setView] = useState("list");
  const [editingSpec, setEditingSpec] = useState(null);
  const [search, setSearch] = useState("");
  const [filterMake, setFilterMake] = useState("");
  const [filterYear, setFilterYear] = useState("");
  const [toast, setToast] = useState({ message: "", visible: false, type: "success" });
  const [loading, setLoading] = useState(true);
  const [saving, setSaving] = useState(false);
  const [connected, setConnected] = useState(true);
  const fileInputRef = useRef(null);

  const showToast = useCallback((msg, type = "success") => {
    setToast({ message: msg, visible: true, type });
    setTimeout(() => setToast((t) => ({ ...t, visible: false })), 2800);
  }, []);

  // Save tech name
  const handleSetName = (name) => {
    setTechName(name);
    localStorage.setItem("alignspec_tech_name", name);
  };

  // Load specs from Supabase
  const loadSpecs = useCallback(async () => {
    try {
      const data = await fetchAllSpecs();
      setSpecs(data);
      setConnected(true);
    } catch (err) {
      console.error("Failed to load:", err);
      setConnected(false);
      showToast("Failed to connect to database", "error");
    } finally {
      setLoading(false);
    }
  }, [showToast]);

  useEffect(() => {
    if (techName) loadSpecs();
  }, [techName, loadSpecs]);

  // Real-time subscription
  useEffect(() => {
    if (!techName) return;
    const channel = supabase
      .channel("alignment_specs_changes")
      .on("postgres_changes", { event: "*", schema: "public", table: "alignment_specs" }, () => {
        loadSpecs(); // reload on any change
      })
      .subscribe();
    return () => { supabase.removeChannel(channel); };
  }, [techName, loadSpecs]);

  const handleNewSpec = () => {
    setEditingSpec(emptySpec(techName));
    setView("form");
  };

  const handleEditSpec = (spec) => {
    const clone = JSON.parse(JSON.stringify(spec));
    // Ensure spec_data has all fields (for older records that might be missing some)
    clone.spec_data = { ...emptySpecData(), ...clone.spec_data };
    if (clone.spec_data.front) clone.spec_data.front = { ...emptySpecData().front, ...clone.spec_data.front };
    if (clone.spec_data.rear) clone.spec_data.rear = { ...emptySpecData().rear, ...clone.spec_data.rear };
    setEditingSpec(clone);
    setView("form");
  };

  const handleDuplicateSpec = (spec) => {
    const dup = JSON.parse(JSON.stringify(spec));
    dup.id = null; // Will create new
    dup.year = "";
    dup.model = dup.model + " (copy)";
    dup.created_by = techName;
    dup.created_at = null;
    dup.updated_at = null;
    dup.spec_data = { ...emptySpecData(), ...dup.spec_data };
    if (dup.spec_data.front) dup.spec_data.front = { ...emptySpecData().front, ...dup.spec_data.front };
    if (dup.spec_data.rear) dup.spec_data.rear = { ...emptySpecData().rear, ...dup.spec_data.rear };
    setEditingSpec(dup);
    setView("form");
    showToast("Duplicated — edit the copy below");
  };

  const handleSaveSpec = async (spec) => {
    setSaving(true);
    try {
      await upsertSpec(spec);
      await loadSpecs();
      setView("list");
      setEditingSpec(null);
      showToast(spec.id ? "Spec updated" : "New spec saved");
    } catch (err) {
      console.error("Save failed:", err);
      showToast("Save failed — check connection", "error");
    } finally {
      setSaving(false);
    }
  };

  const handleDeleteSpec = async (id) => {
    try {
      await deleteSpec(id);
      await loadSpecs();
      showToast("Spec deleted");
      if (view === "form") { setView("list"); setEditingSpec(null); }
    } catch (err) {
      showToast("Delete failed", "error");
    }
  };

